export type TextKey = keyof typeof TEXT.en

function isLanguage(value: string | null): value is Language {
  return value === 'en' || value === 'de'
}

export function getLanguage(): Language {
  const stored =
    typeof localStorage === 'undefined' ? null : localStorage.getItem(LANGUAGE_KEY)
  if (isLanguage(stored)) return stored

  const browserLanguage =
    typeof navigator === 'undefined' ? '' : navigator.language.toLowerCase()
  return browserLanguage.startsWith('de') ? 'de' : 'en'
}

export function setLanguage(language: Language): void {
  if (typeof localStorage === 'undefined') return
  localStorage.setItem(LANGUAGE_KEY, language)
}

export function t(key: TextKey): string {
  return TEXT[getLanguage()][key]
}

export function languageLabel(language: Language): string {
  return language === 'de' ? 'Deutsch' : 'English'
}
